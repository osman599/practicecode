public class ifElseifStatement {

    public static void main (String[]args){
        String city = "Dhaka";
        if (city=="Ctg"){
            System.out.println("City is Ctg");
        } else if (city=="Khulna"){
            System.out.println("City is Khulna");
        } else if (city=="Sylhet") {
            System.out.println("City is Sylhet");
        } else {
            System.out.println(city);
        }
    }
}
