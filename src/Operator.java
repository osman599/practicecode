public class Operator {
    public static void main (String[]args){
        int a=9;
        int b=6;
        int result;
        result=a+b;
        System.out.println("a+b="+result);
        result=a-b;
        System.out.println("a-b="+result);
        result=a*b;
        System.out.println("a*b="+result);
        result=a/b;
        System.out.println("a/b="+result);

    }
}
